var sentence = "Hello, this is a sample sentence for demonstration.";

// Split the string into an array of words using space as the delimiter
var wordsArray = sentence.split(" ");

// Print the array of words
console.log(wordsArray);

// Split the string into an array of sentences using comma as the delimiter
var sentencesArray = sentence.split(",");

// Print the array of sentences
console.log(sentencesArray);